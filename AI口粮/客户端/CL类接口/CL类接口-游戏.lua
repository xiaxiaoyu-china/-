    CL:ResetWndPos(FriendWindow.WndHandle)
    CL:Log("恢复位置")
    -- 恢复窗口上次保存位置  
    CL:RestoreWndPos(FriendWindow.WndHandle) 
end

function FriendWindow.closeWnd(_Handle)
    GUI:WndClose(FriendWindow.WndHandle)
    CL:Log("保存位置")
